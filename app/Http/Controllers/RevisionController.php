     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request, Animal $animal)
    {
        $r = new Revision();
        $r->fecha = $request->fecha;
        $r->descripcion = $request->descripcion;
        $r->animal_id = $animal->id;
        $r->save();
